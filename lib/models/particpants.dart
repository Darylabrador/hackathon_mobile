class Participants {
  final int id;
  final String email;
  final String surname;
  final String firstname;
  final String gender;
  final String role;
  final String teamName;
  final String createdAt;

  Participants({
    required this.id,
    required this.email,
    required this.surname,
    required this.firstname,
    required this.gender,
    required this.role,
    required this.teamName,
    required this.createdAt,
  });
}
